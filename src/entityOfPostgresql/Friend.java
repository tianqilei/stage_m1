package entityOfPostgresql;

public class Friend {

	private String idUsr1;
	private String idUsr2;

	public Friend(String idUsr1, String idUsr2) {
		super();
		this.idUsr1 = idUsr1;
		this.idUsr2 = idUsr2;
	}

	public String getIdUsr1() {
		return idUsr1;
	}

	public void setIdUsr1(String idUsr1) {
		this.idUsr1 = idUsr1;
	}

	public String getIdUsr2() {
		return idUsr2;
	}

	public void setIdUsr2(String idUsr2) {
		this.idUsr2 = idUsr2;
	}

	@Override
	public String toString() {
		return "Friend [idUsr1=" + idUsr1 + ", idUsr2=" + idUsr2 + "]";
	}

	public String toOutputString(String seperator, String nextLine) {
		return getIdUsr1() + seperator + getIdUsr2() + nextLine;
	}
}
